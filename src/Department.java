    public class Department {
    private String phy = "Physics Department";
    private String chem = "Chemistry Department";
    private String cs = "Computer Science Department";
    private String math = "Mathematics Department";
    private String stat = "Statistics Department";

    public Department() {
    }

    public void setPhy(String phy) {
        this.phy = phy;
    }

    public String getPhy() {
        return phy;
    }
    
    public void setChem(String chem) {
        this.chem = chem;
    }

    public String getChem() {
        return chem;
    }

    public void setCs(String cs) {
        this.cs = cs;
    }

    public void setMath(String math) {
        this.math = math;
    }

    public void setStat(String stat) {
        this.stat = stat;
    }

    public String getCs() {
        return cs;
    }

    public String getMath() {
        return math;
    }

    public String getStat() {
        return stat;
    }


    


    
}

    

